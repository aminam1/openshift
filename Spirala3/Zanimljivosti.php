<p> 1. Naziv Bosna dolazi od indo-europske riječi Bosana koja znači voda. Bosna je prepuna prelijepih rijeka, jezera i vodopada (jedan od njih ima visinu preko 300 metara), kao i obala Jadranskog mora. Herzegovina dolazi od imena Herceg Stjepan, drevnog vođe.

 <p> 2. Sarajevo je bilo domaćin Olimpijskih igara 1984. godine. Ove igre obilježile su to vrijeme jer je to bilo prvi put da jedna socijalistička zemlja bude domaćin olimpijskim igrama. Organizatori su u potpunosti iskoristili ovu priliku, te se u to vrijeme izgradilo mnogo sportskih objekata.
