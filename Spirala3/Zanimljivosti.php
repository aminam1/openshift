5. Sarajevska Sahat kula je povijesno nasljeđe za koje se smatra da je jedini javni sat u svijetu po lunarnom vremenu.
</p>
<p>
6. Međugorje se nalazi u planinama u blizini Mostara. Mali grad koji je nevjerojatno popularan osobito među kršćanskim hodočasnicima. Milijuni hodočasnika su ga počeli posjećivati od 1981. godine, kada je jedna manja grupa mladih u ovom selu počela objavljivat da im se ukazala Sveta Gospa.
</p>
<p>
7. Perućica je jedna od posljednjih prašuma u Europi. Nalazi se u Bosni i Hercegovini, u blizini granice sa Crnom Gorom i dio je Nacionalnog parka Sutjeska.  Tu se nalazi i najviša Norveška jela s visinom od 63 metra. 1952.godine, ovo šumsko područje proglašeno je prostorom koji je neophodno znanstveno istraživati i proučavati. Prostire se na čak 1291 hektru.
</p>
