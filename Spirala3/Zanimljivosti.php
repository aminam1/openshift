8. U gradiću Konjicu, koji se nalazi pored rijeke Neretve, nalazi se nuklearni bunker poznat pod nazivom Atomska ratna komanda, koji je izgrađen tijekom revolucije u Jugoslaviji i predsjednika Josipa Broza Tita. Ovo sklonište zauzima površinu od 6.500 metara kvadratnih i sastoji se od 12 povezanih blokova. Izgleda kao komplicirani labirnit, sa stambenim područjima, konferencijskim dvoranama, uredima, sobama za strateško planiranje i ostalim funkcionalnim područjima. Konstrukcija i postojanje ovog bunkera bilo je tajno sve do 1990. godine, kada je konačno otkriven javnosti. Danas je u ovom bunkeru izložba suvremene umjetnosti.
