<p>3. Tramvaji koji su se prvi put koristili u Europi, bili su oni u  Sarajevu, s početkom još 1885. godine. Ova vozila javnog prijevoza su predvodile životinje, najčešće konji, a ponekad i mazge, obično po dvije koje su vozile u timu. 11 godina kasnije, Sarajevo je bilo prvi grad u Europi koji je imao potpuno električnu tramvajsku liniju.
</p>
<p>
4. Bosna i Hercegovina se nalazi na 10. mjestu u svijetu po konzumaciji kave. Bosanska kafa je dio bh. identiteta. To je simbol države i dio njene tradicije, ali predstavlja odraz gurmanskog mentaliteta ovdašnjih stanovnika.
</p>

